async function callClaude(messages, systemPrompt, useWebSearch = false) {
  const tools = useWebSearch ? [{ type: "web_search_20250305", name: "web_search" }] : undefined;
  const body = {
    model: "claude-sonnet-4-6",
    max_tokens: 1000,
    system: systemPrompt,
    messages,
  };
  if (tools) body.tools = tools;

  const res = await fetch("https://api.anthropic.com/v1/messages", {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify(body),
  });
  const data = await res.json();
  return (data.content || []).filter(b => b.type === "text").map(b => b.text).join("\n") || "⚠️ لم أتمكن من المعالجة.";
}

