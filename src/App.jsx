function PDFUploader({ onPDFLoad }) {
  const [dragging, setDragging] = useState(false);
  const [fileName, setFileName] = useState(null);
  const fileRef = useRef();

  const processFile = (file) => {
    if (!file || file.type !== "application/pdf") return alert("الرجاء رفع ملف PDF فقط.");
    setFileName(file.name);
    const reader = new FileReader();
    reader.onload = (e) => onPDFLoad(file.name, e.target.result.split(",")[1]);
    reader.readAsDataURL(file);
  };

  return (
    <div
      onDragOver={(e) => { e.preventDefault(); setDragging(true); }}
      onDragLeave={() => setDragging(false)}
      onDrop={(e) => { e.preventDefault(); setDragging(false); processFile(e.dataTransfer.files[0]); }}
      onClick={() => fileRef.current.click()}
      style={{
        border: `2px dashed ${dragging ? C.accent : C.border}`,
        borderRadius: 16, padding: "32px 24px", textAlign: "center",
        cursor: "pointer", background: dragging ? "#0c1f3a" : C.card,
        transition: "all 0.2s",
      }}
    >
      <input ref={fileRef} type="file" accept=".pdf,image/*" style={{ display: "none" }}
        onChange={(e) => processFile(e.target.files[0])} />
      <div style={{ fontSize: 40 }}>📎</div>
      {fileName
        ? <div style={{ color: C.green, fontWeight: 700, marginTop: 10, fontSize: 15 }}>✅ {fileName}</div>
        : <>
          <div style={{ marginTop: 10, fontSize: 15, color: C.text }}>اسحب PDF أو صورة من المنهج هنا</div>
          <div style={{ marginTop: 6, fontSize: 13, color: C.muted }}>أو اضغط لاختيار ملف • PDF / JPG / PNG</div>
        </>}
    </div>
  );
}

